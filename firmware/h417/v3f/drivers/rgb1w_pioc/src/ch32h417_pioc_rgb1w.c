#include "ch32h417_pioc_rgb1w.h"
#include "ch32h417_pioc.h"
#include "ch32h417_rcc.h"

#define PIOC_RGB1W_SFR_ADDR            ((volatile uint8_t *)&(PIOC->D8_DATA_REG0))
#define PIOC_RGB1W_RAM_ADDR            ((volatile uint8_t *)(PIOC_SRAM_BASE + 0x400u))
#define PIOC_RGB1W_CMD_RAM             0x80u
#define PIOC_RGB1W_CMD_SFR_IO1         0x40u
/* IO1 command values are preserved from WCH EVT PIOC_1_Wire and verified on PF13. */
#define PIOC_RGB1W_CMD_RAM_IO1_100MHZ  0x55u
#define PIOC_RGB1W_CYC_RAM_IO0_100MHZ  117u
#define PIOC_RGB1W_COMMAND             (PIOC->D8_CTRL_WR)

const ch32h417_pioc_rgb1w_pin_t ch32h417_pioc_rgb1w_pin_pf13 =
{
    GPIOF,
    RCC_HB2Periph_GPIOF,
    GPIO_Pin_13,
    GPIO_PinSource13,
    GPIO_AF5,
    CH32H417_PIOC_RGB1W_CHANNEL_IO1
};

static uint8_t rgb1w_io_enable(const ch32h417_pioc_rgb1w_pin_t *pin)
{
    return (pin->channel == CH32H417_PIOC_RGB1W_CHANNEL_IO1) ? RB_MST_IO_EN1 : RB_MST_IO_EN0;
}

static void rgb1w_copy_to_pioc(volatile uint8_t *dest, const uint8_t *src, uint16_t bytes)
{
    while(bytes != 0u)
    {
        *dest = *src;
        dest++;
        src++;
        bytes--;
    }
}

void ch32h417_pioc_rgb1w_init(const ch32h417_pioc_rgb1w_pin_t *pin)
{
    GPIO_InitTypeDef gpio = {0};

    if(pin == 0)
    {
        return;
    }

    RCC_HB2PeriphClockCmd(RCC_HB2Periph_AFIO | pin->port_clock, ENABLE);
    RCC_HBPeriphClockCmd(RCC_HBPeriph_PIOC, ENABLE);

    GPIO_PinAFConfig(pin->port, pin->pin_source, pin->alternate_function);
    gpio.GPIO_Pin = pin->pin;
    gpio.GPIO_Mode = GPIO_Mode_AF_PP;
    gpio.GPIO_Speed = GPIO_Speed_Very_High;
    GPIO_Init(pin->port, &gpio);

    PIOC->D8_SYS_CFG = RB_MST_RESET | RB_MST_IO_EN0 | RB_MST_IO_EN1;
    rgb1w_copy_to_pioc((volatile uint8_t *)PIOC_SRAM_BASE,
                       pioc_rgb1w_code,
                       (uint16_t)sizeof(pioc_rgb1w_code));
}

uint8_t ch32h417_pioc_rgb1w_wait(uint32_t timeout_loops)
{
    if(timeout_loops == 0u)
    {
        while((PIOC->D8_SYS_CFG & RB_INT_REQ) == 0u)
        {
        }
    }
    else
    {
        while((PIOC->D8_SYS_CFG & RB_INT_REQ) == 0u)
        {
            timeout_loops--;
            if(timeout_loops == 0u)
            {
                ch32h417_pioc_rgb1w_halt();
                return CH32H417_PIOC_RGB1W_ERR_TIMEOUT;
            }
        }
    }

    return PIOC->D8_CTRL_RD;
}

uint8_t ch32h417_pioc_rgb1w_send_sfr(const ch32h417_pioc_rgb1w_pin_t *pin,
                                     const uint8_t *data,
                                     uint16_t bytes,
                                     uint32_t timeout_loops)
{
    uint8_t io_enable;

    if((pin == 0) || (bytes == 0u) || (bytes > CH32H417_PIOC_RGB1W_SFR_SIZE))
    {
        return CH32H417_PIOC_RGB1W_ERR_PARAM;
    }

    io_enable = rgb1w_io_enable(pin);
    PIOC->D8_SYS_CFG = RB_MST_RESET | io_enable;
    PIOC->D8_SYS_CFG = RB_MST_CLK_GATE | io_enable;

    if(data != 0)
    {
        rgb1w_copy_to_pioc(PIOC_RGB1W_SFR_ADDR, data, bytes);
    }

    if(pin->channel == CH32H417_PIOC_RGB1W_CHANNEL_IO1)
    {
        PIOC_RGB1W_COMMAND = ((uint8_t)bytes) | PIOC_RGB1W_CMD_SFR_IO1;
    }
    else
    {
        PIOC_RGB1W_COMMAND = (uint8_t)bytes;
    }

    return ch32h417_pioc_rgb1w_wait(timeout_loops);
}

uint8_t ch32h417_pioc_rgb1w_send_ram(const ch32h417_pioc_rgb1w_pin_t *pin,
                                     const uint8_t *data,
                                     uint16_t bytes,
                                     uint32_t timeout_loops)
{
    uint8_t io_enable;

    if((pin == 0) || (bytes == 0u) || (bytes > CH32H417_PIOC_RGB1W_RAM_SIZE))
    {
        return CH32H417_PIOC_RGB1W_ERR_PARAM;
    }

    io_enable = rgb1w_io_enable(pin);
    PIOC->D8_SYS_CFG = RB_MST_RESET | io_enable;

    if(data != 0)
    {
        rgb1w_copy_to_pioc(PIOC_RGB1W_RAM_ADDR, data, bytes);
    }

    PIOC->D8_SYS_CFG = RB_MST_CLK_GATE | io_enable;
    PIOC->D16_DATA_REG0_1 = bytes;

    if(pin->channel == CH32H417_PIOC_RGB1W_CHANNEL_IO1)
    {
        PIOC->D8_DATA_REG2 = 1u;
        PIOC_RGB1W_COMMAND = PIOC_RGB1W_CMD_RAM_IO1_100MHZ | PIOC_RGB1W_CMD_RAM;
    }
    else
    {
        PIOC->D8_DATA_REG2 = 0u;
        PIOC_RGB1W_COMMAND = PIOC_RGB1W_CYC_RAM_IO0_100MHZ | PIOC_RGB1W_CMD_RAM;
    }

    return ch32h417_pioc_rgb1w_wait(timeout_loops);
}

void ch32h417_pioc_rgb1w_halt(void)
{
    PIOC->D8_SYS_CFG &= ~RB_MST_CLK_GATE;
}
