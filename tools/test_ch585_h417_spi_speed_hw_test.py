PROTO_H = os.path.join(ROOT, "hw_tests", "common", "ch585_h417_spi_speed_proto.h")


def read_text(path):
    with io.open(path, "r", encoding="utf-8") as handle:
        return handle.read()


class Ch585H417SpiSpeedHwTestPolicy(unittest.TestCase):
    def test_shared_protocol_is_hw_test_owned(self):
        proto = read_text(PROTO_H)

        self.assertIn("CH585_H417_SPI_SPEED_FRAME_BYTES 192U", proto)
        self.assertIn("CH585_H417_SPI_SPEED_TRANSFER_BYTES 193U", proto)
        self.assertIn("CH585_H417_SPI_SPEED_FRAME_OFF", proto)
        self.assertIn("CH585_H417_SPI_SPEED_MAGIC", proto)
        self.assertIn("CH585_H417_SPI_SPEED_READY_BYTE", proto)
        self.assertIn("CH585_H417_SPI_SPEED_PAYLOAD_OFF", proto)
        self.assertIn("ch585_h417_spi_speed_fixed_byte", proto)
        self.assertIn("ch585_h417_spi_speed_payload_byte", proto)
        self.assertIn("ch585_h417_spi_speed_crc16", proto)
        self.assertNotIn("ADC", proto)
        self.assertNotIn("USBHS", proto)

    def test_ch585_spi0_slave_hw_test_entry(self):
        makefile = read_text(CH585_MAKEFILE)
        common_h = read_text(CH585_COMMON_H)
        source = read_text(CH585_TEST)
        driver = read_text(CH585_SPI0_LINK_DRIVER)

        self.assertIn("ch585_spi0_speed_slave", makefile)
        self.assertIn("CH58x_spi0.c", makefile)
        self.assertIn("ch585_spi0_slave_link.c", makefile)
        self.assertIn("void ch585_spi0_speed_slave_run(void);", common_h)
        self.assertIn("#include \"ch585_spi0_slave_link.h\"", source)
        self.assertIn("#include \"ch585_h417_spi_speed_proto.h\"", source)
        self.assertIn("SPI0_SlaveInit();", driver)
        self.assertIn("ch585_spi0_speed_init_fixed_transfer", source)
        self.assertIn("CH585_H417_SPI_SPEED_READY_BYTE", source)
        self.assertIn("CH585_H417_SPI_SPEED_TRANSFER_BYTES", source)
        self.assertNotIn("ch585_spi0_speed_send_ready", source)
        self.assertIn("ch585_h417_spi_speed_fixed_byte", source)
        self.assertNotIn("ch585_h417_spi_speed_payload_byte", source)
        self.assertIn("ch585_spi0_slave_link_serve_tx_frame", source)
        self.assertIn("s_ch585_spi0_slave_link_aborts", driver)
        self.assertIn("saw_select", driver)
        self.assertIn("R8_SPI0_BUFFER", driver)
        self.assertIn("RB_SPI_IF_FIFO_OV", driver)
        self.assertIn("SetFirstData", driver)
        self.assertIn("PA12_CS_PA13_SCK_PA14_MOSI_PA15_MISO", source)
        self.assertIn("ch585_log_str", source)
        self.assertNotIn("USBHS", source)
        self.assertNotIn("ads7948", source.lower())

