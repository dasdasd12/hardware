import unittest


ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), os.pardir))
CH585_MAKEFILE = os.path.join(ROOT, "hw_tests", "ch585", "Makefile")
CH585_COMMON_H = os.path.join(ROOT, "hw_tests", "ch585", "src", "ch585_common.h")
CH585_TEST = os.path.join(ROOT, "hw_tests", "ch585", "src", "ch585_spi0_speed_slave.c")
CH585_SPI0_LINK_DRIVER = os.path.join(ROOT, "firmware", "ch585", "drivers", "ch585_spi0_slave_link.c")
